package com.trust.tournament.model;

public class RegistrationModel {
    String whatsappNmber,userName,uid,androidId;

    public RegistrationModel() {
    }

    public String getWhatsappNmber() {
        return whatsappNmber;
    }

    public void setWhatsappNmber(String whatsappNmber) {
        this.whatsappNmber = whatsappNmber;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getAndroidId() {
        return androidId;
    }

    public void setAndroidId(String androidId) {
        this.androidId = androidId;
    }
}
